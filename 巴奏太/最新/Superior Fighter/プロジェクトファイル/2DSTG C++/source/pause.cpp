//=============================================================================
// コンストラクタ
//=============================================================================
CPause::CPause()
{
	m_bPause = false;
}

//=============================================================================
// デストラクタ
//=============================================================================
CPause::~CPause()
{

}

//=============================================================================
// 初期化処理
//=============================================================================
void CPause::Init(void)
{

}

//=============================================================================
// 終了処理
//=============================================================================
void CPause::Uninit(void)
{

}

//=============================================================================
// 更新処理
//=============================================================================
void CPause::Update(void)
{
	CInputKeyboard *pKey = CManager::GetInputKeyboard();
	if (pKey->GetTrigger(DIK_P)) {
		TogglePause();
	}
}

//=============================================================================
// 生成関数
//=============================================================================
CPause *CPause::Create(void)
{
	// 生成、初期化
	CPause *pPause;
	pPause = new CPause;
	if (pPause != NULL)
	{
		pPause->Init();
	}

	return pPause;
}

//=============================================================================
// ポーズ状態切り替え
//=============================================================================
void CPause::TogglePause(void)
{
	m_bPause = !m_bPause;

	if (m_bPause)
		ShowCursor(true);
	else
		ShowCursor(false);
}